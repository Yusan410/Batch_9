package day4_operator;

public class PostAndpreFix {

	public static void main(String[] args) {
		
		
		
		int numA = 10;
		numA++;
		System.out.println(numA);
		
		System.out.println(numA++);
        System.out.println(numA--);
	
         System.out.println(numA--);	
	
         int a = 10;
        
        ++a;
        System.out.println(a); 
         
        
        System.out.println(++a);
	
	System.out.println(--a);
	System.out.println(a);
	
	System.out.println(a--);
	
	System.out.println(a);
	
	
	
	}
}
