package internationalWomensDay;

public class IfStatment {
	public static void main(String[] args) {
		
		
        String str = "GeeksforGeeks";
        int i = 4;
 
        // if block
        if (i == 4) {
            i++;
            System.out.println(str);
        }
 
        // Executed by default
        System.out.println("i = " + i);
    }
}

