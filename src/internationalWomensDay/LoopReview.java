package internationalWomensDay;

public class LoopReview {

	public static void main(String[] args) {

		for (int i = 0; i <= 100; i++) {

			if (i % 2 == 0) {

				System.out.println("Codility"+i);

			} else if (i % 3 == 0) {

				System.out.println("SDET"+i);

			} else if (i % 5 == 0) {

				System.out.println("Selenium"+i);
			}
			
		
		}

	}

}
